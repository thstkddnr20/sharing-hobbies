package com.toyproject.sh.domain;

import jakarta.persistence.*;

@Entity
public class TagManager {

    @Id @GeneratedValue
    private Long id;

    @ManyToOne
    @JoinColumn(name = "tag_id")
    private Tag tag;

    @ManyToOne
    @JoinColumn(name = "post_id")
    private Post post;

    @ManyToOne
    @JoinColumn(name = "member_id")
    private Member member;
}
